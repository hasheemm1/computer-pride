export function ErrorBoundary({ error }: Route.ErrorBoundaryProps) {
  let message = "Oops!";
  let details = "An unexpected error occurred.";
  let stack: string | undefined;

  if (isRouteErrorResponse(error)) {
    message = error.status === 404 ? "404" : "Error";
    details =
      error.status === 404
        ? "The requested page could not be found."
        : error.statusText || details;
  } else if (import.meta.env.DEV && error && error instanceof Error) {
    details = error.message;
    stack = error.stack;
  }

  return (
    <div className="gradient-hero min-h-screen flex items-center justify-center p-4">
      <div className="text-center">
        <h1 className="font-heading text-8xl font-bold text-gradient mb-4">{message}</h1>
        <p className="text-xl text-gray-400 mb-8">{details}</p>
        <a href="/" className="btn-primary">
          Return Home
        </a>
        {stack && (
          <pre className="mt-8 p-4 bg-surface-900 rounded-xl text-left overflow-x-auto text-sm text-gray-400 max-w-2xl mx-auto">
            <code>{stack}</code>
          </pre>
        )}
      </div>
    </div>
  );
}
